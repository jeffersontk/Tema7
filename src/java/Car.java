public class Car {
     private int id;
     private String factory;
     private String model;
     private int year;
     private String category;
     private boolean cdPlayer;
     private boolean dvdplayer;
     private String available;
     private String imgCar;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the factory
     */
    public String getFactory() {
        return factory;
    }

    /**
     * @param factory the factory to set
     */
    public void setFactory(String factory) {
        this.factory = factory;
    }

    /**
     * @return the model
     */
    public String getModel() {
        return model;
    }

    /**
     * @param model the model to set
     */
    public void setModel(String model) {
        this.model = model;
    }

    /**
     * @return the year
     */
    public int getYear() {
        return year;
    }

    /**
     * @param year the year to set
     */
    public void setYear(int year) {
        this.year = year;
    }

    /**
     * @return the category
     */
    public String getCategory() {
        return category;
    }

    /**
     * @param category the category to set
     */
    public void setCategory(String category) {
        this.category = category;
    }

    /**
     * @return the cdPlayer
     */
    public boolean isCdPlayer() {
        return cdPlayer;
    }

    /**
     * @param cdPlayer the cdPlayer to set
     */
    public void setCdPlayer(boolean cdPlayer) {
        this.cdPlayer = cdPlayer;
    }

    /**
     * @return the dvdplayer
     */
    public boolean isDvdplayer() {
        return dvdplayer;
    }

    /**
     * @param dvdplayer the dvdplayer to set
     */
    public void setDvdplayer(boolean dvdplayer) {
        this.dvdplayer = dvdplayer;
    }

    /**
     * @return the available
     */
    public String getAvailable() {
        return available;
    }

    /**
     * @param available the available to set
     */
    public void setAvailable(String available) {
        this.available = available;
    }

    /**
     * @return the imgCar
     */
    public String getImgCar() {
        return imgCar;
    }

    /**
     * @param imgCar the imgCar to set
     */
    public void setImgCar(String imgCar) {
        this.imgCar = imgCar;
    }
}
